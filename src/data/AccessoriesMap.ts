/**
 * Finds an accessory by class name and accessory name.
 * Ignores spaces and underscores in the name comparison.
 */
export const findAccessoryByNameAndClass = (className: string, name: string) => {
    const accessories = accessoriesMap[className as keyof typeof accessoriesMap];
    if (!accessories) return null;
    
    // Normalize the search name by removing spaces and underscores
    const normalizedSearchName = name.replace(/[\s_]/g, '').replace('Shining', '').toLowerCase();
    
    return accessories.find(acc => {
        // Normalize the accessory name by removing spaces and underscores
        const normalizedAccName = acc.name.replace(/[\s_]/g, '').replace('Shining', '').toLowerCase();
        return normalizedAccName === normalizedSearchName;
    }) || null;
}